#pragma once

//--------------------------------------------------------------
/// \brief	    An simple event (without data)
//--------------------------------------------------------------
class CEventBase
{
public:
   //--------------------------------------------------------------
   /// \brief	    Constructor
   /// \param[in] id Event id
   //--------------------------------------------------------------
   CEventBase(int id)
      :m_id(id)
   {
   }

   //--------------------------------------------------------------
   /// \brief	    Destructor
   //--------------------------------------------------------------
   virtual ~CEventBase()
   {
   }

   //--------------------------------------------------------------
   /// \brief	    Id getter
   /// \return     Event id
   //--------------------------------------------------------------
   int getId() const { return m_id; }

private:
   //--------------------------------------------------------------
   /// \brief	    Event id
   //--------------------------------------------------------------
   int m_id;
};
