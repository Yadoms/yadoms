#pragma once

namespace automation
{
   //-----------------------------------------------------
   ///\brief A rule
   //-----------------------------------------------------
   class IRule
   {
   public:
      //-----------------------------------------------------
      ///\brief               Destructor
      //-----------------------------------------------------
      virtual ~IRule() {}

      //-----------------------------------------------------
      ///\brief               Start the rule
      //-----------------------------------------------------
      virtual void start() = 0;

      //-----------------------------------------------------
      ///\brief               Stop the rule
      //-----------------------------------------------------
      virtual void stop() = 0;
   };
	
} // namespace automation	
	
	